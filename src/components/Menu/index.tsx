function classNames(...classes) {
  return classes.filter(Boolean).join(" ");
}

type Props<T extends { id: unknown; name: string }> = {
  id: string;
  items: T[];
};

export const Menu = <T extends { id: unknown; name: string }>({
  id,
  items,
}: Props<T>) => {
  const { pathname, push } = useRouter();

  const [selected, setSelected] = useState<T>();

  useEffect(() => {
    if (selected) {
      push({
        pathname,
        query: {
          [id]: `${selected.id}`,
        },
      });
    }
  }, [selected]);

  return (
    <Listbox value={selected} onChange={setSelected}>
      {({ open }) => (
        <div className="relative">
          <Listbox.Button className="relative w-56 border-2 border-gray-300 rounded-full shadow-sm pl-3 pr-10 py-2 text-left cursor-default focus:outline-none focus:ring-1 focus:ring-indigo-500 focus:border-indigo-500 sm:text-sm">
            <Text size="lg" color="white" className="block" overflow="truncate">
              {selected?.name || "Choose a value"}
            </Text>
            <Flex
              align="center"
              className="ml-3 absolute inset-y-0 right-0 pr-2 pointer-events-none"
            >
              <SelectorIcon
                className="h-5 w-5 text-gray-400"
                aria-hidden="true"
              />
            </Flex>
          </Listbox.Button>

          <Transition
            show={open}
            as={Fragment}
            leave="transition ease-in duration-100"
            leaveFrom="opacity-100"
            leaveTo="opacity-0"
          >
            <Listbox.Options className="absolute z-10 mt-1 w-56 bg-white shadow-lg max-h-56 rounded-md py-1 text-base ring-1 ring-black ring-opacity-5 overflow-auto focus:outline-none sm:text-sm">
              {items.map((item) => (
                <Listbox.Option
                  key={`${item.id}`}
                  className={({ active }) =>
                    classNames(
                      active ? "text-white bg-indigo-600" : "text-gray-900",
                      "cursor-default select-none relative py-2 pl-3 pr-9"
                    )
                  }
                  value={item}
                >
                  {({ selected, active }) => (
                    <>
                      <Text
                        weight={selected ? "semibold" : "normal"}
                        overflow="truncate"
                        transform="uppercase"
                        className={"ml-3 block"}
                      >
                        {item.name}
                      </Text>

                      {selected ? (
                        <span
                          className={classNames(
                            active ? "text-white" : "text-indigo-600",
                            "absolute inset-y-0 right-0 flex items-center pr-4"
                          )}
                        >
                          <CheckIcon className="h-5 w-5" aria-hidden="true" />
                        </span>
                      ) : null}
                    </>
                  )}
                </Listbox.Option>
              ))}
            </Listbox.Options>
          </Transition>
        </div>
      )}
    </Listbox>
  );
};
